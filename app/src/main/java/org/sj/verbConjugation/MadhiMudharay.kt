package org.sj.verbConjugation

class MadhiMudharay {
    var hua: String? = null
    var huma: String? = null
    var hum: String? = null
    var hia: String? = null
    var humaf: String? = null
    var hunna: String? = null
    var anta: String? = null
    var antuma: String? = null
