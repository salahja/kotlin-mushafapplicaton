    var anti: String? = null
    var antumaf: String? = null
    var antunna: String? = null
    var ana: String? = null
    var nahnu: String? = null

    constructor()
    constructor(
        hua: String,
        huma: String,
        hum: String,
        hia: String,
        humaf: String,
        hunna: String,
        anta: String,
        antuma: String,
        antum: String,
        anti: String,
        antumf: String,
        antunna: String,
        ana: String,
        nahnu: String,
               ) {
        this.hua = hua
        this.huma = huma
        this.hum = hum
        this.hia = hia
        this.humaf = humaf
        this.hunna = hunna
        this.anta = anta
        this.antuma = antuma
        this.antum = antum
        this.anti = anti
        this.antumaf = antumf
        this.antunna = antunna
        this.ana = ana
        this.nahnu = nahnu
    }
}

